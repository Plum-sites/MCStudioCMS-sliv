<span class="punish">Наказание:</span> Кик с сервера / Блокировка игрового чата или аккаунта до 24 часов.<br>
<strong>5.4</strong> Запрещено выпрашивать что-либо у представителей команды проекта.<br>
<span class="punish">Наказание:</span> Кик с сервера / Блокировка игрового чата до 3 часов.<br>
<strong>5.5</strong> Запрещено отправлять запросы на телепортацию представителям команды проекта.<br>
<span class="punish">Наказание:</span> Кик с сервера / Временная блокировка аккаунта до 30 минут.<br>
<strong>5.6</strong> Запрещено выдавать себя за представителя команды проекта, даже в качестве шутки.<br>
<span class="punish">Наказание:</span> Кик с сервера / Блокировка аккаунта до 12 часов.<br>
<strong>5.7</strong> Запрещается вводить в заблуждение команду проекта по техническим и игровым аспектам. Также запрещены сообщения в текстовом чате (локальный, глобальный, торговый, личный чат) 'Умею дюпать', 'Научи дюпать', 'Скину читы' и т.п. даже в шуточной форме.<br>
<span class="punish">Наказание:</span> Блокировка аккаунта до 5 суток.
